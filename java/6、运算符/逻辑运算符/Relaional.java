public class Relaional {
    public static void main(String[] args) {
        int n = 1;
        int m = 1;
        System.out.println(m>n);
        System.out.println(m>=n);
        System.out.println(m==n);
        System.out.println(m<=n);
        System.out.println(m<n);
        System.out.println(m!=n);
    }
}