public class Float {
    public static void main(String[] args) {
        float a = 1.1F;
        double b = 3.2;
        double c = .32;
        double d = 3.2e2;
        double e = 3.2e-2;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        double num1 = 2.7;
        double num2 = 8.1 / 3;
        System.out.println(num1);
        System.out.println(num2);
    }
}